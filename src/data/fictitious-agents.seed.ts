  const agents: AgentDTO[] = [];
  let globalIndex = 0;

  // Helper to add agents from coordinate array
  const addAgentsFromCoords = (coords: [number, number][], count: number) => {
    for (let i = 0; i < count && i < coords.length; i++) {
      const [lat, lng] = coords[i];
      agents.push({
        id: `npc-${globalIndex}`,
        lat: lat + (Math.random() - 0.5) * 0.02, // Small random offset
        lng: lng + (Math.random() - 0.5) * 0.02,
        username: generateAgentName(globalIndex),
        agent_code: generateNPCCode(globalIndex),
        status: getRandomStatus(globalIndex),
        lastSeen: new Date(Date.now() - Math.random() * 86400000).toISOString(), // Random time in last 24h
        rank_id: Math.floor(Math.random() * 5) + 1
      });
      globalIndex++;
    }
  };

  // Add agents by region
  addAgentsFromCoords(EUROPE_COORDS, 250);      // 250 Europe
  addAgentsFromCoords(USA_COORDS, 350);         // 350 USA
  addAgentsFromCoords(SOUTH_AMERICA_COORDS, 200); // 200 South America
