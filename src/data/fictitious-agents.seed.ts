// Helper to generate multiple points around a city center
function generateCityPoints(centerLat: number, centerLng: number, count: number, spread: number = 0.08): [number, number][] {
  const points: [number, number][] = [];
  for (let i = 0; i < count; i++) {
    const angle = (i / count) * Math.PI * 2 + Math.random() * 0.5;
    const distance = Math.random() * spread;
    points.push([
      centerLat + Math.sin(angle) * distance + (Math.random() - 0.5) * 0.02,
      centerLng + Math.cos(angle) * distance + (Math.random() - 0.5) * 0.02
    ]);
  }
  return points;
}

